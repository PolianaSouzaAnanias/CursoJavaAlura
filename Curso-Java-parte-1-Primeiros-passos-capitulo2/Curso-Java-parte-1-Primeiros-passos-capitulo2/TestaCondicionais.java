
public class TestaCondicionais {

	public static void main(String[] args) {
		System.out.println("testando condicionais");
        int idade = 20;
        if (idade >= 18) {
            System.out.println("você tem mais de 18 anos");
            System.out.println("seja bem vindo");
        }
	}
}

